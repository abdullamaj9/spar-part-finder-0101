const CATEGORY_NAMES = Object.keys(PART_CATEGORIES);

function partsForCategory(cat) {
  return PART_CATEGORIES[cat] || [];
}

// قائمة مسطّحة بكل القطع (للبحث)
function allParts() {
  const out = [];
  for (const cat of CATEGORY_NAMES) {
    for (const p of PART_CATEGORIES[cat]) out.push({ category: cat, part: p });
  }
  return out;
}

