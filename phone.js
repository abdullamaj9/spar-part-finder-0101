// تطبيع رقم الواتساب لصيغة Meta الدولية الموحّدة
// يحذف: المسافات، الشرطات، الأقواس، +، والأصفار البادئة (00)
// النتيجة: رقم دولي نظيف مثل 971566350025
function normalizePhone(raw) {
  if (!raw) return '';
  let n = String(raw).trim();
  // إزالة كل ما ليس رقمًا (مسافات، +، شرطات، أقواس)
  n = n.replace(/[^\d]/g, '');
  // إزالة 00 البادئة (صيغة الاتصال الدولي)
  if (n.startsWith('00')) n = n.slice(2);
  // معالجة الأرقام المحلية الإماراتية: لو بدأ بـ 0 (مثل 0566350025) واتبعه 5، نحوّله لـ 971
  if (n.startsWith('0') && n.length === 10) n = '971' + n.slice(1);
  // لو بدأ بـ 5 مباشرة (566350025) بطول 9، نضيف 971
  if (n.length === 9 && n.startsWith('5')) n = '971' + n;
  return n;
}

module.exports = { normalizePhone };
